// ── Contacts ──
export const fetchContacts = () => apiGet<Contact[]>('/contacts')
export const fetchContact = (id: string) => apiGet<Contact>(`/contacts/${id}`)
export const fetchInteractions = (id: string) => apiGet<Interaction[]>(`/contacts/${id}/interactions`)
export const fetchDecisions = (id: string) => apiGet<Decision[]>(`/contacts/${id}/decisions`)
export const draftEmail = (contactId: string) => apiPost<Record<string, unknown>>(`/contacts/${contactId}/draft-email`)
export const executeAction = (contactId: string, action: string, decisionId?: string, overrideReason?: string) =>
  apiPost<Record<string, unknown>>(`/contacts/${contactId}/execute-action`, {
    action,
    decision_id: decisionId ?? null,
    override_reason: overrideReason ?? null,
  })
export const postCallAnalysis = (contactId: string, transcript: string) =>
  apiPost<Record<string, unknown>>(`/contacts/${contactId}/post-call`, { transcript })

// ── Decisions ── (evaluate)
export const evaluateContact = (contactId: string) => apiPost<EvaluationResult>(`/decisions/evaluate/${contactId}`)

// ── Guidelines ──
export const fetchGuidelines = () => apiGet<Guidelines>('/guidelines')
export const updateGuidelines = (body: Partial<Guidelines>) => apiPut<Guidelines>('/guidelines', body)
export const fetchActivePreset = () => apiGet<ActivePreset>('/guidelines/active')
export const fetchPresetList = () => apiGet<{ presets: string[] }>('/guidelines/presets')
export const switchPreset = (name: string) => apiPost<{ status: string; preset: string; guidelines: Guidelines }>(`/guidelines/preset/${name}`)

// ── Decisions ── (override)
export const overrideDecision = (decisionId: string, action: string, reason: string) =>
  apiPost<{ status: string; decision_id: string }>(`/decisions/${decisionId}/override`, { action, reason })

// ── Contracts ──
export async function analyzeContract(file: File) {
  const form = new FormData()
  form.append('file', file)
  const res = await fetch(apiUrl('/contracts/analyze'), { method: 'POST', body: form })
  if (!res.ok) throw new Error(`API ${res.status}: ${await res.text()}`)
  return res.json()
}
export async function analyzeContractForContact(contactId: string, file: File) {
  const form = new FormData()
  form.append('file', file)
  const res = await fetch(apiUrl(`/contracts/analyze/${contactId}`), { method: 'POST', body: form })
  if (!res.ok) throw new Error(`API ${res.status}: ${await res.text()}`)
  return res.json()
}

