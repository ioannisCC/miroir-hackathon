/**
 * API client for the Miroir backend.
 * Uses VITE_API_URL or defaults to http://localhost:8000.
 */

const API_BASE =
  (typeof import.meta !== 'undefined' && (import.meta as { env?: { VITE_API_URL?: string } }).env?.VITE_API_URL) ||
  'http://localhost:8000'

export function apiUrl(path: string): string {
  const p = path.startsWith('/') ? path : `/${path}`
  return `${API_BASE}${p}`
}

export async function apiGet<T = unknown>(path: string): Promise<T> {
  const res = await fetch(apiUrl(path))
  if (!res.ok) {
    const text = await res.text()
    throw new Error(`API ${res.status}: ${text || res.statusText}`)
  }
  return res.json() as Promise<T>
}

export async function apiPost<T = unknown>(path: string, body?: unknown): Promise<T> {
  const res = await fetch(apiUrl(path), {
    method: 'POST',
    headers: body ? { 'Content-Type': 'application/json' } : undefined,
    body: body ? JSON.stringify(body) : undefined,
  })
  if (!res.ok) {
    const text = await res.text()
    throw new Error(`API ${res.status}: ${text || res.statusText}`)
  }
  return res.json() as Promise<T>
}

export async function apiPut<T = unknown>(path: string, body: unknown): Promise<T> {
  const res = await fetch(apiUrl(path), {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  })
  if (!res.ok) {
    const text = await res.text()
    throw new Error(`API ${res.status}: ${text || res.statusText}`)
  }
  return res.json() as Promise<T>
}

// ---------------------------------------------------------------------------
// Types — must match Supabase table columns exactly
// ---------------------------------------------------------------------------

export type Contact = {
  id: string
  name: string
  email: string
  phone?: string | null
  behavior_profile: Record<string, unknown>
  risk_score?: number | null
  trust_score?: number | null
  use_case?: string | null
  created_at?: string
  updated_at?: string
}

export type Interaction = {
  id: string
  contact_id: string
  type: string                // "call" | "email"
  summary: string | null
  transcript: string | null
  outcome: string | null
  timestamp: string           // auto-generated by Supabase
}

/**
 * Decision row from Supabase `decisions` table.
 *
 * DB columns: id, contact_id, approach_chosen, reasoning,
 * confidence_score, confidence_notes, escalate, outcome,
 * outcome_notes, created_at.
 */
export type Decision = {
  id: string
  contact_id: string
  approach_chosen: string     // action value e.g. "send_email"
  reasoning: string | null
  confidence_score: number | null
  confidence_notes: string | null
  escalate: boolean | null
  outcome: string | null
  outcome_notes: string | null
  created_at: string
}

/**
 * Shape returned by POST /decisions/evaluate/{contact_id}.
 * This is the pipeline result, NOT a DB row.
 */
export type EvaluationResult = {
  action: string
  reasoning: string | null
  confidence: number
  confidence_notes: string
  escalate: boolean
  overrode_pass1: boolean
  override_reason: string | null
  pass1_recommendation: Record<string, unknown>
  pass2_recommendation: Record<string, unknown>
}

